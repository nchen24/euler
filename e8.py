def product_of_string(s):
    """Find the product of all of the integers that make up a string

    Args:
        s (string): The string to parse and multiply.  Behavior is undefined
        when not all characters are numbers.

    Returns:
        int: The product of all of the numbers that make up `s`
    """

    return reduce(lambda x, y: x*y, [int(c) for c in s])

def find_max_product(s, chunk_len):
    """Find the maximum product of `chunk_len` characters in `s`

    Args:
        s (string): The string to parse
        chunk_len (int): The length of the chunk to be multiplied.  Behavior is
        undefined when `chunk_len` > len(`s`)

    Returns:
        int: The maximum product of `chunk_len` consecutive integers within `s`
    """

    cur_max = 0
    for i in range(len(s) - chunk_len + 1):
        cur_max = max(cur_max, product_of_string(s[i:i+chunk_len]))
    return cur_max

